import tkinter


# 第一个tkinter的界面
# application [ˌæplɪ'keɪʃ(ə)n] 应用程序
def application():
    # 创建一个窗口对象
    top = tkinter.Tk()
    # 设置主循环
    top.mainloop()


if __name__ == "__main__":
    # 调用函数
    application()
